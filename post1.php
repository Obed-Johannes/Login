            <h2> China's Digital Police State </h2>

Obed Johannes<br>
Analytical Reading and Writing<br> 
Professor Sterling<br>
Oct 19, 2022<br>

<br>
<br>

<p>    The Internet was started in the 1960s as a way for government researchers to be able to
share information quickly. It has developed into a global interconnected network where anyone, 
anywhere, has access to what is estimated to be over 5 million terabytes of data. With this 
much data readily available to nearly anyone, fear arose in those with authoritative positions. 
The need to control what its citizens have access to and monitor what they say or do has become 
a top priority for many nations. The Internet has brought a new era of instant data transmission 
where the world is at many of our fingertips; it has also ushered us into an era of mass surveillance
and rampant censorship where you never know if you genuinely have privacy. Like many other nations, 
China has joined the digital world; however, they have taken measures to ensure that the CCPs interests 
are protected, even if it is at its citizens' expense, widespread virtual invasion of their citizens' 
personal data is used to maintain a strong grip over the nation. This level of invasiveness is necessary 
if the CCP wants to solidify its role as an overarching patriarchal figure, an entity which wants its citizens
 to believe it knows what's best for them.</p>

<p>    With the birth of the Internet came the need for digital surveillance. Hagen, Janne, and Lysne paper
Protecting the Digitized Society—the Challenge of Balancing Surveillance and Privacy, published in 2016 
discusses the problems which arose with the globalization of the internet and how internet censoring and 
mass surveillance developed as a result of that. Borders are almost non-existent now as anyone anywhere 
can use the internet as a means to bypass physical barriers and reach people across the globe. This had an 
effect on how governments could control its citizens and made it so the option was less privacy for more 
security. Policing the Internet became something nations found necessary in maintaining a converter presence
on the Internet, locating and predicting crimes before they happen(Hagen, Janne, and Lysne 82). 
Governments have infringed on citizens' privacy to ensure social security. Virtual propaganda and 
other sponsored media can affect other nations' elections. Some nations use flooding social networks with
conflicting and inflammatory information to incite unrest in other countries. China uses these “threats” 
to their national security as a way to legitimize their potential overstep on people's privacy. It 
legitimizes the rather intrusive nature they take in social monitoring.</p> 

<div class="banner-left-Post1"></div>

<p>     China has historically used walls as a defensive method, keeping accurate to that. The Great Will of Fire,
a nickname given to the CCP's Internet surveillance and censorship program, officially known as China's
National Public Network Surveillance System (Zixue TAI 64), acts as a filter for the entire Internet blocking
specific sites. Sites deemed potentially harmful or morally objectionable by the CCP are stopped before they 
can reach the people of China. The CCP uses software and hardware configurations to pinpoint digital content 
(Zixue TAI 65). Major western-based corporations supplied much of this high-tech software and hardware. "The 
United States, the United Kingdom, and Israel. In particular, US-based Cisco has designed and supplied about 
80 percent of the 'mirror routers' currently in China to eavesdrop on Internet transmissions (Israel, 2009; 
Santoro, 2009)." (Zixue TAI 65) Although these corporations have had a significant influence on the technology 
available to the CCP surveillance system, emerging Chinese companies like Huawei, TP-LINK, and Tenda have 
further advanced the field of cyber surveillance. Multiple collaborative projects between these Chinese 
companies and Western institutions have even developed "bio-chemical, artificial intelligence, and auto-matic 
recognition surveillance technologies," all of which have been implemented into the Chinese Great 
Firewall(Zixue TAI 65).</p> 

<p>     The Golden Project started the development of invasive technologies, with multiple subsidiary groups like the 
Golden Bridge, an interconnected communications network that links the provinces and cities of China; the 
Golden Card, a digital monetary transfer network servicing the Chinese national banks. Some overarching goals 
of these projects were, first, interconnecting public security (i.e., Chinese police) via multimedia 
communications. This has helped the Chinese government compile data on citizens through claims of national 
security (Zixue TAI 68).They also hoped to promote a national standard of inter- agency information-sharing 
across the country, certifying network professionals within China, enhancing network safety, and, most 
importantly, monitoring real-time internet traffic to keep any unwanted content from entering (Zixue TAI 69).</p> 

<p>     The CCP has reasons it goes to these lengths to censor and regulate information available to its citizens. 
Social media and other forms of global communication allowed political dissidence to proliferate in Chinese 
society. Rogier Creems article The Pivot in Chinese Cybergovernance: Integrating Internet Control in Xi 
Jinping's China, discusses China's efforts to maintain a firm grip on information shared in China through 
the internet. He also details how the Chinese government has restricted and limited access to certain 
information in the pursuit of ideological dominance over the nation. China's fears of foreign nations using 
the internet to influence its people and potentially causing harm to the structures of power in place in 
China. The CCP had two main focal points when approaching social media's role in China. First, official 
voices were to gain more prominence in the online sphere, while non-official voices were to be co-opted into 
subscribing to government-approved ideologies. Secondly, online anonymity was targeted as a whole. People 
were forced to register with real names to make identifying and persecuting enviable actions 
easier(Creemers 10).</p> 

<p>     With minor exceptions, foreign satellite broadcasts are not available to ordinary Chinese. In 1993 and 1994, 
the government introduced regulations to forbid individual households from setting up satellite dishes to get 
programs from Hong Kong, Taiwan, Japan, or the United States. CNN and BBC TV are only available to hotel 
patrons (The Times, Jun 5 1999)(Lam 43). The CCP made sure that information allowed to circulate around China 
was pre-curated to conform to Communist sentiments. State-controlled media avoid topics like casualties from 
fires and floods; even Chinese history is approached very carefully, making sure to steer clear of dwelling on 
topics like the start of the Cultural Revolution, the liberal aspects of what became known as the Chinese 
Enlightenment (Lam 44).</p> 

<p>     The strict Chinese approach to digital surveillance forces their citizens to use the Internet as if they are 
being constantly monitored. Their government has constructed this panoptic situation they have found 
themselves in. Information is heavily regulated, making their view of the world biased and skewed depending 
on what the CCP determines benefits them. Regulation of speech and immediate action against any form of 
dissent helps protect the regime's powers, further cementing its control.</p> 

